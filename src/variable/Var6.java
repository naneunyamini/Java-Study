package variable;

public class Var6 {
    public static void main(String[] args) {
        // 변수에 값을 직접 넣는 것을 리터럴이라고 칭함
        int a = 100; //정수
        double b = 10.5; //실수
        boolean c = true; //불리언 true,false 입력 가능
        char d = 'A'; // 문자 하나(작은 따옴표)
        String e = "Hello Java"; //문자열을 다루기 위한 특별한 타입(큰 따옴표)

        System.out.println(a); //command + d 하면 복사됨
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
